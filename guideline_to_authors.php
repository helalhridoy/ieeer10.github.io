          <div class="card">
            <div class="card-header">Corresponding Author</div>
            <div class="card-content">
              <p>Each paper submitted to COMPAS must have a designated corresponding author assigned.</p>
              <p>While it is traditional for the first listed author to be the corresponding author, any author of the
                paper may be designated the corresponding author.</p>
              <p>Whereas many of the responsibilities of the corresponding authorwill require compliance with legal
                terms and conditions, such as registration and transferring copyright, the corresponding author of the
                paper should be an adult of 18 years of age or older with the authority to agree to any and all legal
                requirements for the paper.</p>
              <p>The corresponding author acts as the assigned manager of the paper and should have the authority of all
                authors to speak collectively on behalf of all authors.</p>
              <p>The corresponding author is the author responsible for completing all tasks and expectations required
                for the paper during the paper submission process.</p>
              <p>The corresponding author is responsible for all activities related to the paper submission. This
                includes but is not limited to:</p>
              <ul>
                <li>The initial submission of a compliant manuscript on or before the submission deadline.</li>
                <li>Ensure adherence to the deadlines during the paper submission process.</li>
                <li>Making timely updates or changes to the paper that may be requested or required during the
                  submission process.</li>
                <li>Acting as the representative to all authors associated with the paper as the lead communicator and
                  representative for the paper.</li>
                <li>Ensuring any and all fees related to the submission, such as conference registration fee and
                  submission fees, are paid to support final paper upload in accordance with conference deadlines.</li>
                <li>Completing the accurate final submission of the paper on or before the final submission deadline.
                </li>
                <li>Ensuring the paper is presented at the conference during the assigned presentation time scheduled by
                  the organizing committee.</li>
                <li>Adherence to the COMPAS No Show Policy and processes therein.</li>
              </ul>
            </div>
          </div>
          <div class="card">
            <div class="card-header">Paper Initial Submission</div>
            <div class="card-content">
              <p><strong>Timely Submission</strong></p>
              <p>All authors who wish to have a paper considered for inclusion in the conference program must submit and
                complete the paper submission process before the submission deadline for the respective submission type.
                Late submissions will not be accommodated.</p>
              <p><strong>Submission Quality</strong></p>
              <p>Authors are encouraged to complete a thorough review of the initial submission manuscript. The paper
                which is submitted will be the paper that is reviewed by the reviewing body. Once the initial submission
                deadline closes any requests to edit the initial submission cannot be accommodated.</p>
              <p>Reviews are assigned by theme and keywords included in the paper data during the initial paper
                submission process. Authors are encouraged to carefully select keywords and themes to ensure the paper
                is reviewed by peers in your subject area.</p>
              <p><strong>Review Process</strong></p>
              <p>Each paper will be reviewed by reviewers assigned by the IEEE COMPAS Editorial Board team in connection
                with the conference organizing committee and in compliance with IEEE Publication and Review Policies.
              </p>
              <p>Reviews are traditional single-blind peer reviews where the identity of the reviewer is not visible to
                the author(s).</p>
              <p>The qualitative and quantitative feedback from the reviews and other factors of consideration for the
                conference will be gathered to determine whether the papers should be either accepted or rejected to the
                conference.</p>
              <p><strong>Acceptance or Rejection</strong></p>
              <p>On or before the scheduled and communicated notification date the conference will notify the
                corresponding author of the acceptance or rejection of the paper along with any comments and/or
                suggested feedback received from reviewers. If accepted, we strongly encourage updating the paper with
                any recommendations provided by the reviewers.</p>
              <p>Papers that are accepted will be advised of the next steps and will be required to move forward with
                additional requirements and deadlines related to the conference submission process.</p>
              <div class="card">
                <div class="card-header">Final Submission</div>
                <div class="card-content">
                  <p>Papers that are accepted to the conference program are required to complete all steps outlined for
                    the final submission process. Papers that do not complete the final submission steps on or before
                    the deadlines are subject to removal from the conference program.</p>
                  <p><strong>Submit the Final Draft:</strong></p>
                  <p>Submit the final draft on or before the published deadline. The Final draft should be Presentation
                    and Publication Ready.</p>
                  <p>We encourage a complete and thorough review of the paper as this will be the final opportunity to
                    make any changes, adjustments, or acknowledgments; Requests to update, edit, change, or add
                    information to the paper will not be accommodated after the final submission deadline.</p>
                  <p><strong>Transfer of Copyright to IEEE:</strong></p>
                  <p>In accordance with IEEE Policy and the IEEE PSPB Operations Manual, all papers must transfer
                    copyright of the paper to IEEE before publication. You may visit the IEEE Author center for more
                    details on the copyright process and requirements.</p>
                  <p><strong>Registration to the Conference:</strong></p>
                  <p>The corresponding author of the paper is expected to register to attend the conference and present
                    the work.</p>
                  <p>If the corresponding author is not able to attend the conference, he or she may delegate this
                    responsibility to a co-author. The co-author attending is then required to pay the registration.
                    This supports the rule that at least one author of the paper must pay the full registration fee in
                    support of the paper.</p>
                  <p>Delegation of this responsibility requires that the corresponding author submit a
                    substitute/absentee case in advance.</p>
                  <p>Full registration rates are defined as rates which offer all full access to the conference for the
                    duration of the conference.</p>
                  <p>Student Full rates can support a full paper.</p>
                  <p><strong>All fees must be paid in full, including any applicable taxes and additional fees.</strong>
                  </p>
                  <p>Each Author Full Registration will support 1 Accepted Papers of any submission type.</p>
                  <p>Additional submission fees will apply to any submission greater than 1.</p>
                  <p>Full papers that exceed 6 pages in length will be charged an over-length page fee, per page count
                    greater than 6.</p>
                  <p>Over-length page fees are applied regardless of page content. For example, a 7-page paper that
                    contains 6 pages of research content and 1 reference page is considered a 7-page paper and will
                    incur overlength page charges.</p>
                  <p>The Maximum Page Length is 9 pages inclusive of the reference page.</p>
                  <p>Overlength Paper Charges:</p>
                  <ul>
                    <li>One additional page: $250.00</li>
                    <li>Two additional pages: $500.00</li>
                    <li>Three additional pages: $750.00</li>
                  </ul>
                </div>
              </div>
              <div class="card">
                <div class="card-header">Paper Presentation</div>
                <div class="card-content">
                  <p>Each paper that is accepted to the conference and meets the final submission and registration
                    requirements will be required to present their work at the conference as scheduled by the organizing
                    committee.</p>
                  <p>Decisions on the Presentation type assigned to a paper are determined by the conference organizing
                    committee considering the factors which include
                    , but are not limited to:</p>
                  <ul>
                    <li>Conference Editorial Board recommendation</li>
                    <li>Recommendations and suggestions by the paper reviewer</li>
                    <li>Topic and content of the paper</li>
                    <li>Topic interest</li>
                    <li>Conference delivery resources</li>
                  </ul>
                  <p>Given the size of the conference and the number of papers presented during the conference, the
                    committee is unable to accommodate special requests for presentation types and/or scheduling
                    accommodations.</p>
                  <p>Authors are encouraged to plan attendance for the full conference scheduled as all presentation
                    dates are subject to change with or without notice.</p>
                </div>
              </div>
              <div class="card">
                <div class="card-header">COMPAS No Shows Policy</div>
                <div class="card-content">
                  <p><strong>Policy:</strong></p>
                  <p>COMPAS enforces a no-show policy. Any paper which is accepted to the conference, completes final
                    submission, and is included in the final program must be presented at the conference.</p>
                  <p>The corresponding author is the scheduled presenter of the paper and is required to present the
                    paper at the scheduled presentation date and time determined by the conference organizing committee.
                  </p>
                  <p>The author(s) of any paper which is added to the final conference program but is not presented as
                    scheduled will be subscribed to the no-show list compiled by EMBS.</p>
                  <p><strong>COMPAS No-Show Author List:</strong></p>
                  <p>Authors who are listed on the COMPAS no-show list will be included on said list for a period of no
                    less than 2 years.</p>
                  <p>The list of no-shows will be made available to all Region 10 Conference organizers, and said
                    organizers may reject submissions from authors included on the no-show list.</p>
                  <p><strong>Substitutions:</strong></p>
                  <p>The corresponding author is required to present the paper. Should any circumstances arise where the
                    corresponding author is unable to present the paper at the conference, the corresponding author may
                    delegate the presentation responsibility to one of the listed co-authors of the paper to present the
                    paper in his or her absence.</p>
                  <p>Said substitution of a corresponding author by a co-author must be documented and substantiated in
                    advance via the Substitute/Absentee Form.</p>
                  <p>It is the responsibility of the corresponding author to submit a substitute/absentee case to the
                  COMPAS organizer before the scheduled date of the presentation.</p>
                  <p>Undocumented substitutions will be considered a corresponding author "no show".</p>
                  <p>Should a circumstance arise where none of the listed authors are able to present the paper at the
                    conference, it is the responsibility of the corresponding author to submit a substitute/absentee
                    case via the COMPAS Substitute/Absentee Form, documenting the reason for the absence.</p>
                  <p>Cases submitted to the COMPAS Substitute Absentee Form, where the paper is not presented by the
                    corresponding author or a substitute co-author, will be reviewed on its merits, and supporting
                    documentation may be required.</p>
                </div>
              </div>

