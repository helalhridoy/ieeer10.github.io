                <li>Be compliant with the IEEE Code of Ethics</li>
                <li>All authors listed on the paper must be eligible to submit a paper to COMPAS 23.</li>
              </ul>
              <p><strong>Formatting</strong></p>
              <ul>
                <li>All papers must be written in English</li>
                <li>All papers must be original unpublished work</li>
                <li>Each paper may be written by up to 35 authors maximum.</li>
                <li>Each author listed on the paper must be listed in the paper management system</li>
                <li>One author must be designated as the corresponding author for the paper.</li>
                <li>Our current paper management system will accept papers in Microsoft Word and Latex.</li>
                <li>We highly recommend the usage of the provided templates to generate your paper.</li>
                <li>Templates can be downloaded here and from the paper submission system.</li>
                <li>Each submission will be reviewed for compliance with paper size, margins, headers, footers, page
